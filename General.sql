--CREATE A FUNCTION TO GET LIST OF EMPLOYEES BY PASSING DEPARTMENT ID.

CREATE FUNCTION ALLEMPLOYEES(@ID VARCHAR(50))
RETURNS @RESULT TABLE 
(ID INT ,NAME VARCHAR(50),SALARY DECIMAL,DEPTID VARCHAR(50),PHONE VARCHAR(10))
AS 
   BEGIN
      INSERT INTO @RESULT
	  SELECT * FROM EMPLOYEE WHERE EMP_ID = @ID;
	  RETURN
END;

