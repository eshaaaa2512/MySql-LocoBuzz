---CREATE A FUNCTION TO GET EMPLOYEE SALARY Y PASSING EMPLOYEE NAME

CREATE FUNCTION SALARY(@NAME AS VARCHAR(50))
RETURNS DECIMAL 

BEGIN
        DECLARE @SAL DECIMAL;
		SELECT @SAL =EMP_SALARY FROM EMPLOYEE
		WHERE EMP_NAME = @NAME;
		RETURN @SAL;
END

