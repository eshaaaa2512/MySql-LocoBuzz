
---NUMERIC FUNCTION

SELECT ABS(-9); ---GIVING ABSOLUTE VALUES 
SELECT CEILING(89.67); ---IT WILL GIVE UPPER VALUE IN INT
SELECT FLOOR(89.67); --- IT WILL GIVE LOWER VALUE IN INT
SELECT SIGN(-6); ---IT WILL GIVE -1 FOR EVERY NEGATIVE NUMBER
SELECT SIGN(6); ---IT WILL GIVE 1 FOR EVERY POSITIVE NUMBER
SELECT SIGN(0);
